    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (rootNodeField.getText() == null || rootNodeField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter Valid Root Node", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            return;
        }
        if (rootNodeField.getText().equals(adminApp.getRootNode())) {
            JOptionPane.showMessageDialog(this, "Same Existing Root Node!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            return;
        }
        try {
            adminApp.setNewRootNode(rootNodeField.getText());
            JOptionPane.showMessageDialog(this,"New Root Node Applied Successfully!");
        } catch (Exception ex) {
            System.out.println("Error " + ex);
            JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
        }
    }//GEN-LAST:event_updateButtonActionPerformed


