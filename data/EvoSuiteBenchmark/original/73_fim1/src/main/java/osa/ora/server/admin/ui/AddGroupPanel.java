    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText() == null || jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter Group Name", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            return;
        }
        boolean found = false;
        for (int i = 0; i < adminApp.getGroups().size(); i++) {
            if (adminApp.getGroups().get(i).getName().equalsIgnoreCase(jTextField1.getText())) {
                found = true;
                break;
            }
        }
        if (!found) {
            Group newGroup = new Group();
            newGroup.setName(jTextField1.getText());
            newGroup.setUsers(new Vector<User>(0));
            try {
                newGroup =adminApp.createGroup(newGroup);
                if(newGroup!=null){
                    adminApp.getGroups().add(newGroup);
                    JOptionPane.showMessageDialog(this,"Group Added Successfully!");
                    jTextField1.setText("");
                }else{
                    JOptionPane.showMessageDialog(this, "Server Error!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
                }
            } catch (Exception ex) {
                System.out.println("Error " + ex);
                JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Group Already Exist!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
        }
    }//GEN-LAST:event_jButton2ActionPerformed
