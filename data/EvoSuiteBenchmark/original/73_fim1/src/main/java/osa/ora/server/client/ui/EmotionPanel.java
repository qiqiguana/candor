    private void addEmotion(String smile) {
        int caretPos = chatPanel.getJEditorPane2().getCaretPosition();
        int totalPos = chatPanel.getJEditorPane2().getText().length();
        if (caretPos == totalPos) {
            chatPanel.getJEditorPane2().setText(chatPanel.getJEditorPane2().getText() + smile);
        } else {
            String text = chatPanel.getJEditorPane2().getText();
            chatPanel.getJEditorPane2().setText(text.substring(0, caretPos) + smile + text.substring(caretPos));
        }
        hidePanel();
    }
    private void hidePanel(){
        parentFrame.setVisible(false);
        chatPanel.getJEditorPane2().setFocusable(true);
    }
