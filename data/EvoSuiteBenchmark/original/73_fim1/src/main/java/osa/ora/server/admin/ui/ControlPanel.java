    private void jButton16ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton16ActionPerformed
        int n = JOptionPane.showConfirmDialog(this, "Are you sure you want to kick off all current logged-in users?", "Kick Off Users", JOptionPane.OK_CANCEL_OPTION);
        if (n == JOptionPane.OK_OPTION) {
            String value = JOptionPane.showInputDialog(this, "Enter Justification:", "Justification", JOptionPane.QUESTION_MESSAGE);
            if (value != null && !value.equals("")) {
                try {
                    adminApp.kickOffUsers(value);
                    JOptionPane.showMessageDialog(this,"All Users Kicked Off!");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
                }
            }else{
               JOptionPane.showMessageDialog(this, "You can't kick off all system users without justification message!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            }
        }
    }//GEN-LAST:event_jButton16ActionPerformed

    private void jButton17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton17ActionPerformed
        getParentDialog().getContentPane().removeAll();
        if(systemUsersPanel==null){
            systemUsersPanel=new SystemUsersPanel(adminApp,this);
        }
        try {
            adminApp.loadGroups();
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Can't Re-Load Users", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
        }
        try {
            adminApp.returnOnlineIPs();
        } catch (RemoteException ex) {
            JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Can't retrieve User Online Status", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
        }
        systemUsersPanel.updateModel();        
        getParentDialog().getContentPane().add(systemUsersPanel);
        getParentDialog().setSize(585, 265);
        getParentDialog().setTitle("System Users");
        getParentDialog().setVisible(true);
    }//GEN-LAST:event_jButton17ActionPerformed

