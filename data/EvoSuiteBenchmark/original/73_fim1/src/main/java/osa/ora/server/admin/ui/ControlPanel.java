    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        int n = JOptionPane.showConfirmDialog(this, "Are you sure you want to Shutdown the server ?", "Stop Server", JOptionPane.OK_CANCEL_OPTION);
        if (n == JOptionPane.OK_OPTION) {
           String value = JOptionPane.showInputDialog(this, "Enter Justification:", "Justification", JOptionPane.QUESTION_MESSAGE);
            if (value != null && !value.equals("")) {
                 try {
                    adminApp.shutdown(value);
                    JOptionPane.showMessageDialog(this,"Server Stopped!");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
                }
            }else{
               JOptionPane.showMessageDialog(this, "You can't shutdown the server without justification message!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            }
        }
    }//GEN-LAST:event_jButton14ActionPerformed

