    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        chatApp.getClientSettingBean().setSourceFontName(sourceFont.getName());
        chatApp.getClientSettingBean().setSourceFontSize(""+sourceFont.getSize());
        chatApp.getClientSettingBean().setSourceFontStyle(""+sourceFont.getStyle());
        chatApp.getClientSettingBean().setSourceFontColor(""+userFontLabel.getBackground().getRGB());
        chatApp.getClientSettingBean().setDestFontName(destFont.getName());
        chatApp.getClientSettingBean().setDestFontSize(""+destFont.getSize());
        chatApp.getClientSettingBean().setDestFontStyle(""+destFont.getStyle());
        chatApp.getClientSettingBean().setDestFontColor(""+receiveFontLabel.getBackground().getRGB());        
        chatApp.getClientSettingBean().updateSettings();
        JOptionPane.showMessageDialog(this,"Font/color Settings Applied Successfully!");
        parentFrame.setVisible(false);
    }//GEN-LAST:event_jButton6ActionPerformed

