    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jPasswordField1.getText()!=null && !jPasswordField1.getText().equals("")){
            if(jPasswordField1.getText().length()<3){
                JOptionPane.showMessageDialog(this,"Default Password Minimum Length is 3!","Change Default Password",JOptionPane.ERROR_MESSAGE);
            }else {
                boolean result=false;
                try {
                    result = adminApp.setNewDefaultPassword(jPasswordField1.getText());
                    if(result){
                        JOptionPane.showMessageDialog(this,"New Default Password Applied Successfully!");
                    }else{
                        JOptionPane.showMessageDialog(this,"Failed to Set New Default Password!");
                    }
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
                }
            }
        }else{
            JOptionPane.showMessageDialog(this,"Please Enter All Fields!","Change Default Password",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed


