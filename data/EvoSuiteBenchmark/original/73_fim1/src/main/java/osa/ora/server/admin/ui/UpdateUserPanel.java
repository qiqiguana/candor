    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        Group group=(Group)jComboBox1.getSelectedItem();
        User user=(User)jComboBox2.getSelectedItem();
        int index=jComboBox2.getSelectedIndex();
        if (user == null) {
            JOptionPane.showMessageDialog(this, "Can't identify selected User!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            return;
        }
        if (jTextField1.getText() == null || jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter User Name", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            return;
        }
        if (jTextField2.getText() == null || jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter User email", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            return;
        }
        for(int i=0;i<adminApp.getGroups().size();i++){
            Vector<User> tempUsers=adminApp.getGroups().get(i).getUsers();
            for(int n=0;n<tempUsers.size();n++){
                if(user.getId()!=tempUsers.get(n).getId()){
                    if(jTextField2.getText().equals(tempUsers.get(n).getEmail())){
                        JOptionPane.showMessageDialog(this, "User with Same email Already Exist!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
                        return;
                    }
                }
            }
        }
        try {
            user.setName(jTextField1.getText());
            user.setEmail(jTextField2.getText());
            user.setDirectPhone(jTextField3.getText());
            if(jTextField3.getText()==null || "".equals(jTextField3.getText())){
                user.setDirectPhone("N/A");   
            }else{
                user.setDirectPhone(jTextField3.getText());
            }
            if(jTextField4.getText()==null || "".equals(jTextField4.getText())){
                user.setJobTitle("N/A");
            }else{
                user.setJobTitle(jTextField4.getText());
            }
            user.setShowMyStatus(jCheckBox1.isSelected());
            user.setOnlyStartChat(jCheckBox2.isSelected());
            user.setCanJoinChatRoom(jCheckBox3.isSelected());
            user = adminApp.updateUser(user);
            if(user!=null){
                group.getUsers().set(index, user);
                if(jComboBox2.getModel().getSize()>0) jComboBox2.setSelectedIndex(0);
                jComboBox2.validate();
                JOptionPane.showMessageDialog(this,"User Updated Successfully!");
            }else{
                JOptionPane.showMessageDialog(this, "Server Error!", "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
            }
        } catch (Exception ex) {
            System.out.println("Error " + ex);
            JOptionPane.showMessageDialog(this, "Error Happen " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE, adminApp.getErrorIcon());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        Group group=(Group)jComboBox1.getSelectedItem();
        jComboBox2.setModel(new javax.swing.DefaultComboBoxModel(group.getUsers()));
        if(group.getUsers()!=null && group.getUsers().size()>0){
            User user=group.getUsers().get(0);
            fillUser(user);
        }else{
            reset();
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged
    private void reset(){
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jCheckBox1.setSelected(false);
        jCheckBox2.setSelected(false);
        jCheckBox3.setSelected(false);
    }
    private void fillUser(User user){
        jTextField1.setText(user.getName());
        jTextField2.setText(user.getEmail());
        jTextField3.setText(user.getDirectPhone());
        jTextField4.setText(user.getJobTitle());
        jCheckBox1.setSelected(user.isShowMyStatus());
        jCheckBox2.setSelected(user.isOnlyStartChat());
        jCheckBox3.setSelected(user.isCanJoinChatRoom());
    }
    private void jComboBox2ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox2ItemStateChanged
        User user=(User)jComboBox2.getSelectedItem();
        if(user!=null){
            fillUser(user);
        }else{
            reset();
        }
    }//GEN-LAST:event_jComboBox2ItemStateChanged


