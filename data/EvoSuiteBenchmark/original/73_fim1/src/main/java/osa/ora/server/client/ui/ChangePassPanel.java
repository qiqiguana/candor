    private void applyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_applyButtonActionPerformed
        if(passwordField1.getText()!=null && !passwordField1.getText().equals("") && passwordField2.getText().equals(passwordField3.getText())){
            if(passwordField2.getText().length()<6){
                JOptionPane.showMessageDialog(this,"Password Length Must Not Less Than 6 Char(s)!","Change Password",JOptionPane.ERROR_MESSAGE);
            }else{
                String result=chatApp.changePassword(passwordField1.getText(),passwordField2.getText());
                JOptionPane.showMessageDialog(this,result);
            }
        }else{
            JOptionPane.showMessageDialog(this,"Password Mismatch!","Change Password",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_applyButtonActionPerformed


