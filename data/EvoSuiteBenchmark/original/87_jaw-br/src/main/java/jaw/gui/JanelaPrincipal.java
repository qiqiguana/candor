    private void abrir(int opcao) {
        ProcessarEntidadesTabela processar = new ProcessarEntidadesTabela();
        int pos = jTabbedPane1.getTabCount();
        jTabbedPane1.add(processar, pos);
        new Abrir(processar, opcao);
        jTabbedPane1.setTitleAt(pos, processar.getNomeArquivoSalvo());
        jTabbedPane1.setSelectedIndex(pos);
        ArquivoRecente.adicionarArquivo(processar.getNomeArquivoSalvo());
        carregarArquivosRecentes();
    }

