    private void iniciarTabela() {
        Mapeamento.carregarMapeamentos();
        DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("Entrada");
        for (Mapeamento mapeamento : jaw.Main.configuracao.getMapeamentos()) {
            model.addColumn(mapeamento);
        }
        TreeSet<String> chaves = Mapeamento.getChaves();
        for (String chave : chaves) {
            Vector valores = new Vector();
            valores.add(chave);
            for (Mapeamento mapeamento : jaw.Main.configuracao.getMapeamentos()) {
                valores.add(mapeamento.getMapeamento().getProperty(chave));
            }
            model.addRow(valores);
        }
        jTable1.setModel(model);
    }

