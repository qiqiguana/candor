    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Remover Mapeamento
        this.salvar();
        int[] colunas = jTable1.getSelectedColumns();
        List<Mapeamento> lista = new Vector();
        for (int i = 0; i <
                colunas.length; i++) {
            if (colunas[i] == 0) {
                continue;
            }

            Mapeamento mapeamento = jaw.Main.configuracao.getMapeamentos().get(colunas[i] - 1);
            lista.add(mapeamento);
        }

        for (Mapeamento mapeamento : lista) {
            int opc = javax.swing.JOptionPane.showConfirmDialog(null, "Realmente deseja remover o mapeamento \"" + mapeamento + "\"?", "", JOptionPane.YES_NO_OPTION);
            if (opc == JOptionPane.OK_OPTION) {
                mapeamento.remover();
                this.iniciarTabela();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // remover chave - valor
        this.salvar();
        int linha = jTable1.getSelectedRow();
        if (linha < 0) {
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Deseja remover?") != JOptionPane.OK_OPTION) {
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Mapeamento.removerChave(model.getValueAt(linha, 0).toString());
        this.iniciarTabela();
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //adicionar chave - valor
        this.salvar();
        Mapeamento.adicionarChave(jTextField1.getText());
        this.iniciarTabela();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int novaColuna = -1;
        for (int i = 0; i <
                model.getRowCount(); i++) {
            if (model.getValueAt(i, 0).toString().equals(jTextField1.getText())) {
                novaColuna = i;
                break;
            }

        }
        for (int i = 0; i <
                model.getColumnCount(); i++) {
            jTable1.changeSelection(novaColuna, i, true, false);
        }
    }//GEN-LAST:event_jButton6ActionPerformed

