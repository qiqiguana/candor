    private void salvar(){
        List<Mapeamento> lista = new Vector();
        for (int i = 0; i < jTable1.getColumnCount(); i++) {
            if (i == 0) {
                continue;
            }
            Mapeamento mapeamento = jaw.Main.configuracao.getMapeamentos().get(i - 1);
            for (int j = 0; j < jTable1.getRowCount(); j++) {
                String chave = jTable1.getValueAt(j,0).toString();
                String valor = jTable1.getValueAt(j,i).toString();
                mapeamento.getMapeamento().setProperty(chave,valor);
            }
            lista.add(mapeamento);
        }
        for (Mapeamento mapeamento : lista) {
            mapeamento.salvar();
        }
    }
    
