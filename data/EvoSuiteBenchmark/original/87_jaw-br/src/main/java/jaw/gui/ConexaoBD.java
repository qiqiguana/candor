    private void driverFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_driverFocusLost
        try {
            if (this.driver.getModel().getSelectedItem().toString().equals("org.postgresql.Driver")) {
                this.caminho.setText("jdbc:postgresql://localhost:5432/");
            } else if (this.driver.getModel().getSelectedItem().toString().equals("com.mysql.jdbc.Driver")) {
                this.caminho.setText("jdbc:mysql://localhost/");
            }
        } catch (Exception e) {
            jaw.Main.janelaPrincipal.log(e.toString());
        }
}//GEN-LAST:event_driverFocusLost

