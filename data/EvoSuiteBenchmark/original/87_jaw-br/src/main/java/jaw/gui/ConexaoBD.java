    private void conectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_conectarActionPerformed
        // TODO add your handling code here:
        if (!this.driver.getSelectedItem().toString().equals("Selecione o Driver")) {
            jaw.entrada.Conexao conexao = new jaw.entrada.Conexao(this.driver.getSelectedItem().toString(),
                    this.caminho.getText(), this.base.getText(),
                    this.usuario.getText(), this.senha.getText());

            conexao.conectar();
            conectado = true;
            this.setVisible(false);
            this.processarEntidade.setNomeArquivoSalvo("");
            while (conexao.isConectado()) {
                
                this.processarEntidade.carregarEntidades(conexao.listarEntidades());
            }
            if (conexao.getAlteracao()) {
                JOptionPane.showMessageDialog(this, "A conexão detectou tipos desconhecidos pela ferramenta.\n" +
                        "É recomendável ajustar os mapeamentos e reimpotar os dados.\n" +
                        "Clique em Configurações -> Mapeamento para ajustar os mapeamentos.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por Favor Selecione Um Driver!");
            return;
        }         
}//GEN-LAST:event_conectarActionPerformed

