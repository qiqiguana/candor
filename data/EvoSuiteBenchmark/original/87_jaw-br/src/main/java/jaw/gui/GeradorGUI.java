    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Gerar
        List<Entidade> selecionadas = new Vector();

        for (int i = 0; i < jTable2.getRowCount(); i++) {
            if (!((Boolean) jTable2.getModel().getValueAt(i, 0)).booleanValue()) {
                continue;
            }
            selecionadas.add((Entidade) jTable2.getModel().getValueAt(i, 1));
        }

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            if (!((Boolean) jTable1.getModel().getValueAt(i, 0)).booleanValue()) {
                continue;
            }
            ModeloDeAplicacao modelo = modelos.get(i);
            new jaw.saida.Gerador(selecionadas, modelo);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

