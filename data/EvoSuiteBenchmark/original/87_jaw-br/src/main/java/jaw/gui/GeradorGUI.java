    private void listarModelos() {
        ((javax.swing.table.DefaultTableModel) jTable1.getModel()).setRowCount(0);
        for (ModeloDeAplicacao modelo: modelos) {
            ((javax.swing.table.DefaultTableModel) jTable1.getModel()).addRow(new Object[]{new Boolean(false), modelo.getNome(), modelo.getDescricao()});
        }
    }

    private void listarEntidades() {
        ((javax.swing.table.DefaultTableModel) jTable2.getModel()).setRowCount(0);
        for (int i = 0; i < entidades.size(); i++) {
            ((javax.swing.table.DefaultTableModel) jTable2.getModel()).addRow(new Object[]{new Boolean(false), entidades.get(i)});
        }
    }

