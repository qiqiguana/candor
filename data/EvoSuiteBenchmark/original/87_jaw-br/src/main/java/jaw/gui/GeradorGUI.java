    private void alterarModeloActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarModeloActionPerformed
        // alterar template
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            if (!((Boolean) jTable1.getModel().getValueAt(i, 0)).booleanValue()) {
                continue;
            }
            ModeloDeAplicacao modelo = modelos.get(i);
            new CriarModeloDeAplicacao(modelo).setVisible(true);
        }
}//GEN-LAST:event_alterarModeloActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // inverter selecao - templates
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            jTable1.getModel().setValueAt(new Boolean(!((Boolean) jTable1.getModel().getValueAt(i, 0)).booleanValue()), i, 0);

        }
    }//GEN-LAST:event_jButton4ActionPerformed

